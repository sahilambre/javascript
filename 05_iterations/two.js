// while
let index = 0;

while (index <= 10) {
  //   console.log(`the value of index is ${index}`);
  index += 2;
}

let myArray = ["flash", "superman", "spiderman"];

let arr = 0;

while (arr < myArray.length) {
  //   console.log(`value is ${myArray[arr]}`);
  arr = arr + 1;
}

let score = 1;
do {
  console.log(`score is ${score}`);
  score += 1;
} while (score < 10);
